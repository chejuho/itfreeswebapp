package board;


public class ConstantBean {
	private Integer idx = new Integer(0);
	private String constance_name = "";
	private String constance_value = "";
	public String getConstance_name() {
		return constance_name;
	}
	public void setConstance_name(String constance_name) {
		this.constance_name = constance_name;
	}
	public String getConstance_value() {
		return constance_value;
	}
	public void setConstance_value(String constance_value) {
		this.constance_value = constance_value;
	}
	public Integer getIdx() {
		return idx;
	}
	public void setIdx(Integer idx) {
		this.idx = idx;
	}
}
